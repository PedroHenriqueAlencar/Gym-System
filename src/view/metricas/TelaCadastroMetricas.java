    //Cadastrar
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if("".equals(mTextField01.getText()))
        {
            JOptionPane.showMessageDialog(null,"Falta inserir o RA!");
        }
        else
        {

            MetricasDAO metricasDAO = new MetricasDAO();
            Metricas metricas = new Metricas();
            metricas.setRa(Integer.parseInt(mTextField01.getText()));
            metricas.setAltura(Double.parseDouble(mTextField02.getText()));
            metricas.setPeso(Double.parseDouble(mTextField03.getText()));
            metricas.setAbdomem(Double.parseDouble(mTextField04.getText()));
            metricas.setQuadril(Double.parseDouble(mTextField05.getText()));
            metricas.setPeitoral(Double.parseDouble(mTextField06.getText()));
            metricas.setBraco(Double.parseDouble(mTextField07.getText()));
            metricas.setPerna(Double.parseDouble(mTextField08.getText()));
            metricas.setDataCadastro(mTextField09.getText());
            metricas.setSituacaoCorporal(mTextField10.getText());
            metricasDAO.inserir(metricas); 

            JOptionPane.showMessageDialog(null,"Métrica cadastrada com sucesso!.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

